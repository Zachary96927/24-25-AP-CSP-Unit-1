import turtle as trtl
painter= trtl.Turtle()
painter.speed(50)

def star():
    painter.begin_fill()
    painter.pendown()
    painter.circle(5)
    painter.pendown()
    painter.end_fill()
# Making the sun

painter.penup()
painter.goto(100, 100)
painter.fillcolor('red')
star()
painter.goto(-150,-164)
star()
painter.goto(-74,136)
star()
painter.goto(187,-45)
star()
painter.goto(212,-103)
star()
